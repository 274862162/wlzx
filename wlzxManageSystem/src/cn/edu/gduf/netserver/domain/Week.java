package cn.edu.gduf.netserver.domain;

public class Week {

	private int weekID;
	private String day;
	
	public int getWeekID() {
		return weekID;
	}
	public void setWeekID(int weekID) {
		this.weekID = weekID;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
	
}
